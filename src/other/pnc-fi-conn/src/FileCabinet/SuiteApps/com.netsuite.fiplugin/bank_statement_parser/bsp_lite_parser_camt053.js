        //This is done to check for what would come first when there are multiple paths mapped to the same field
        module.getStatementStrings = function getStatementStrings(){
            var open = '<Stmt>';
            var close = '</Stmt>';
            var indexOfOpen, indexOfClose;
            while(module.fileContents.indexOf(open) !== -1){
                indexOfOpen = module.fileContents.indexOf(open);
                indexOfClose = module.fileContents.indexOf(close);
                var statement = module.fileContents.slice(indexOfOpen,indexOfClose+7);
                module.statementStrings.push(statement);
                module.fileContents = module.fileContents.substring(indexOfClose+7);
            }
        };

